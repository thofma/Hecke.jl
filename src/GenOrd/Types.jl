@attributes mutable struct GenOrdIdl{S, T}
  order::GenOrd{S, T}
  basis#::Vector{elem_type(GenOrd{S, T}}
  basis_matrix#dense_matrix_type(T)
  basis_mat_inv#dense_matrix_type(S)
  norm
  minimum
  is_prime::Int            # 0: don't know
                           # 1 known to be prime
                           # 2 known to be not prime
  is_zero::Int             # as above
  is_principal::Int        # as above

  gen_one::RingElem
  gen_two::GenOrdElem

  princ_gen::GenOrdElem
  prim_elem::GenOrdElem
  min_poly_prim_elem
  basis_in_prim
  phi

  splitting_type::Tuple{Int, Int}
                         #ordered as ramification index, inertia degree


  function GenOrdIdl(O::GenOrd{S, T}) where {S, T}
    r = new{S, T}()
    r.order = O
    r.is_prime = 0
    r.is_zero = 0
    r.is_principal = 0
    r.splitting_type = (0, 0)
    return r
  end

  function GenOrdIdl(O::GenOrd, M::MatElem)
    # basis_matrix must be an owned (non-view) n x n lower-left HNF matrix
    # ideal(O::GenOrd, M) ensures this
    n = degree(O)
    @assert nrows(M) == n
    @assert ncols(M) == n

    r = GenOrdIdl(O)
    r.basis_matrix = M
    return r
  end

  function GenOrdIdl(O::GenOrd, x::GenOrdElem)
    # create ideal of O generated by x
    @assert parent(x) === O
    r = GenOrdIdl(O)
    r.princ_gen = x
    r.is_principal = 1

    if iszero(x)
       r.is_zero = 1
    end

    r.norm = _make_canonical_in(O, norm(x))
    r.gen_one = r.norm
    r.gen_two = x
    return r
  end

  function GenOrdIdl(O::GenOrd, x::RingElem)
    return GenOrdIdl(O, O(x))
  end

  function GenOrdIdl(O::GenOrd, T::Vector{<:GenOrdElem})
    @assert all(x -> parent(x) === O, T)

    # TODO: One should do this block by block instead of the big matrix
    V = hnf(reduce(vcat, [representation_matrix(x) for x in T]), :lowerleft)
    return ideal(O, V; M_in_hnf = true)
  end

  function GenOrdIdl(O::GenOrd, p::RingElem, a::GenOrdElem)
    r = GenOrdIdl(O)
    r.gen_one = p
    r.gen_two = a
    return r
  end
end

@attributes mutable struct GenOrdFracIdl{S, T}
  order::GenOrd{S, T}
  num::GenOrdIdl{S, T}
  den::RingElem
  norm::RingElem
  basis_matrix#::FakeFracFldMat
  basis_mat_inv#::FakeFracFldMat

  function GenOrdFracIdl(O::GenOrd{S, T}) where {S, T}
    z = new{S, T}()
    z.order = O
    return z
  end

  function GenOrdFracIdl(a::GenOrdIdl{S, T}, b::RingElem) where {S, T}
    z = new{S, T}()
    O = order(a)
    z.order = O
    z.num = a
    z.den = _make_canonical_in(O, b)
    return z
  end

   function GenOrdFracIdl(a::GenOrdIdl{S, T}) where {S, T}
    z = new{S, T}()
    O = order(a)
    z.order = order(a)
    z.num = a
    z.den = O.R(1)
    return z
  end


  function GenOrdFracIdl(O::GenOrd{S, T}, a::MatElem) where {S, T}
    z = new{S, T}()
    z.order = O
    z.basis_matrix = a
    return z
  end
