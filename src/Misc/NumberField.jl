      abs!(oo, tacb)
      log!(oo, oo)
      mul2exp!(oo, oo, 1)
      z[r1 + i] = oo

      #z[r1 + i] = 2*log(abs(evaluate(parent(K.pol)(x), c.complex_roots[i])))
      if !isfinite(z[r1 + i]) || !radiuslttwopower(z[r1 + i], -target_tol)
        abs_tol = 2*abs_tol
        prec_too_low = true
        #@show "restart in the complex"
        break
      end
    end

    if prec_too_low
      continue
    end

    for i in 1:r1 + r2
      #@show z[i]
      #@show -target_tol
      zz = deepcopy(z[i])
      expand!(z[i], -target_tol)
    end
    return z
  end
end

