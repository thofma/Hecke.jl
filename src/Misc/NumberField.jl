doc"""
***
    normal_basis(K::Nemo.AnticNumberField) -> nf_elem
> Given a number field K which is normal over Q, it returns 
> an element generating a normal basis of K over Q
"""
function normal_basis(K::Nemo.AnticNumberField)

  n=degree(K)
  Aut=Hecke.automorphisms(K)
  length(Aut) != degree(K) && error("The field is not normal over the rationals!")

  A=zero_matrix(FlintQQ, n, n)
  r=K(1)
  while true
  
    r=rand(basis(K),-n:n)
    for i=1:n
      y=Aut[i](r)
      for j=1:n
        A[i,j]=coeff(y,j-1)
      end
    end
    if det(A)!=0
      break
    end
  end
  return r
end

doc"""
    compact_presentation(a::FacElem{nf_elem, AnticNumberField}, n::Int = 2; decom, arb_prec = 100, short_prec = 1000) -> FacElem
> Computes a presentation $a = \prod a_i^{n_i}$ where all the exponents $n_i$ are powers of $n$
> and, the elements $a$ are "small", generically, they have a norm bounded by $d^{n/2}$ where
> $d$ is the discriminant of the maximal order.
> As the algorithm needs the factorisation of the principal ideal generated by $a$, it can
> be passed in in \code{decom}.
"""
function compact_presentation(a::FacElem{nf_elem, AnticNumberField}, nn::Int = 2; decom=false, arb_prec = 100, short_prec = 1000)
  n = fmpz(nn)
#  global last_red = (a, n, decom)
  K = base_ring(a)
  ZK = maximal_order(K)
  if typeof(decom) == Bool
    de::Dict{NfOrdIdl, fmpz} = factor_coprime(a, IdealSet(ZK))
  else
    de = Dict((p, v) for (p, v) = decom)
  end

  be = FacElem(K(1))

  v = conjugates_arb_log_normalise(a, arb_prec)
  _v = maximum(abs, values(de))+1
  #Step 1: reduce the ideal in a p-power way...
  A = ideal(ZK, 1)
  for _k = floor(Int, log(Int(n), Int(_v))):-1:0
    B = Dict((p, div(v, Int(n^_k)) % Int(n)) for (p, v) = de)
    if haskey(B, A)
      B[A] += n
    else
      B[A] = n
    end
    A, alpha = reduce_ideal2(FacElem(B))
    be *= alpha^(-(n^_k))
    v -= n^_k .* conjugates_arb_log_normalise(alpha, arb_prec)
  end
  if length(be.fac) > 1
    delete!(be.fac, K(1))
  end
  #Step 2: now reduce the infinite valuation

  r1, r2 = signature(K)
 
  m = maximum(abs, values(de))
  m = max(m, 1)
  mm = abs_upper_bound(log(1+maximum(abs, v))//log(n), fmpz)
  k = max(ceil(Int, log(m)/log(n)), Int(mm))

  de = Dict(A => fmpz(1))
  delete!(de, ideal(ZK, 1))
  B=0
  
  while k>=1
    D = Dict((p, div(fmpz(v), n^k)) for (p, v) = de if v >= n^k)
    if length(D) == 0
      A = FacElem(Dict(ideal(ZK, 1) => 1))
    else
      A = FacElem(D)
    end
    vv = [x//n^k for x = v]
    vvv = fmpz[]
    for i=1:r1
      push!(vvv, round(fmpz, vv[i]//log(2)))
    end
    for i=r1+1:r1+r2
      local r = round(fmpz, vv[i]//log(2)//2)
      push!(vvv, r)
      push!(vvv, r)
    end
    @assert abs(sum(vvv)) <= degree(K)
    b = short_elem(inv(simplify(evaluate(A))), matrix(FlintZZ, 1, length(vvv), vvv), prec = short_prec) # the precision needs to be done properly...
    B = simplify(ideal(ZK, b))
    for p = keys(de)
      local _v = valuation(b, p)
      de[p] += n^k*_v
      B *= inv(p)^_v
    end
    @assert !haskey(de, ideal(ZK, 1))
    for (p, _v) = factor(B)
      @assert !haskey(de, p)
      @assert !isone(p)
      de[p] = _v*n^k
    end
    v_b = conjugates_arb_log_normalise(b, arb_prec)
#    @show old_n = sum(x^2 for x = v)
    v += n^k .* v_b
#    @show new_n = sum(x^2 for x = v)
#    @show old_n / new_n 
#    @assert isone(b) || new_n < old_n  *2
    be  *= FacElem(b)^(n^k)
    k -= 1
  end
  b = evaluate_mod(a*be, B)
  return inv(be)*b
end

